public class Moneda {
    private int Valor;
    private String nombre;

    public Moneda(int Valor, String nombre) {
        this.Valor = Valor;
        this.nombre = nombre;
    }

    public int getValor() {
        return Valor;
    }

    public void setValor(int Valor) {
        this.Valor = Valor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
